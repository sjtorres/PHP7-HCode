<?php
// operadores de comparação
$a = 55.0;
$b = 55;

var_dump($a > $b);
echo "<br>";
var_dump($a < $b);
echo "<br>";
var_dump($a == $b); // dois iguais: operador de comparação
echo "<br>";
var_dump($a === $b); // três iguais: operador de igualdade
echo "<br>";
var_dump($a != $b); // não é igual: operador de negação
echo "<br>";

?>